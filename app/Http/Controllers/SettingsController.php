    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $data = Setting::find($id);
        if(is_null($data)) {
            return response()->json(null, 404);
        }
        return response()->json($data, 200);
    }

